// Function to get a listing by ID
export function getListingById(id: string) {
  return allListings.find(listing => listing.id === id) || null;
}

export function getLocalizedListing(listing: any, locale: string = "en") {
  const t = listing.translations[locale] || listing.translations.en;
  return {
    ...listing,
    title: t.title,
    location: t.location,
    description: t.description,
    amenities: t.amenities,
  }
}

